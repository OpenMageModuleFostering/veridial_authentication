/**
 * Option array for available Methods in Veridial Admin
 *
 * @category   Veridial
 * @package    Liquid11_Veridial
 * @author     Veridial <https://www.veridial.co.uk/>
 */
class Liquid11_Veridial_Model_System_Config_Source_Method
{
	public function toOptionArray($includeEmpty = false, $emptyText = '-- Please Select --')
	{
		$options = array();
		if ($includeEmpty) {
			$options[] = array(
				'value' => '',
				'label' => Mage::helper('adminhtml')->__($emptyText),
			);
		}
		foreach($this->getOptions() as $value => $label) {
			$options[] = array(
				'value' => $value,
				'label' => Mage::helper('adminhtml')->__($label),
			);
		}
		return $options;
	}
	public function getOptions()
	{
		return array(
			'app' => 'App',
            'sms' => 'SMS',
			'voice' => 'Voice',
			'email' => 'Email',
		);
	}
}
